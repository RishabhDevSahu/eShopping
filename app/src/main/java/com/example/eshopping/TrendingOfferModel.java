package com.example.eshopping;

public class TrendingOfferModel {
    private String TrendOfferID;
    private String TrendOfferName;
    private String TrendOfferDiscount;
    private String TrendOfferDescription;
    private String TrendOfferImage;


    public TrendingOfferModel(){

    }

    public String getTrendOfferID() {
        return TrendOfferID;
    }

    public void setTrendOfferID(String trendOfferID) {
        TrendOfferID = trendOfferID;
    }

    public String getTrendOfferName() {
        return TrendOfferName;
    }

    public void setTrendOfferName(String trendOfferName) {
        TrendOfferName = trendOfferName;
    }

    public String getTrendOfferDiscount() {
        return TrendOfferDiscount;
    }

    public void setTrendOfferDiscount(String trendOfferDiscount) {
        TrendOfferDiscount = trendOfferDiscount;
    }

    public String getTrendOfferDescription() {
        return TrendOfferDescription;
    }

    public void setTrendOfferDescription(String trendOfferDescription) {
        TrendOfferDescription = trendOfferDescription;
    }

    public String getTrendOfferImage() {
        return TrendOfferImage;
    }

    public void setTrendOfferImage(String trendOfferImage) {
        TrendOfferImage = trendOfferImage;
    }
}
